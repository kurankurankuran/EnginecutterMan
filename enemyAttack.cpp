#include "enemyAttack.h"

EnemyAttack::EnemyAttack()
{

}
EnemyAttack::~EnemyAttack()
{

}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT EnemyAttack::Init()
{
	HRESULT hr = S_OK;
	hr = m_FallAtk.Init();
	hr = m_HomingAtk.Init();

	return hr;
}
//=============================================================================
// 終了処理
//=============================================================================
void EnemyAttack::Uninit()
{
	m_HomingAtk.Uninit();
	m_FallAtk.Uninit();
}
//=============================================================================
// 更新処理
//=============================================================================
void EnemyAttack::Update()
{
	m_FallAtk.Update();
	m_HomingAtk.Update();
}
//=============================================================================
// 描画処理
//=============================================================================
void EnemyAttack::Draw()
{
	m_FallAtk.Draw();
	m_HomingAtk.Draw();
}